a = 4
b = 8
c = 3

sama_dengan_ab = int(a == b)
lebih_besar_bc = int(b > c)
tidak_sama_ac = int(a != c)

print(f"Variabel a bernilai {a}")
print(f"Variabel b bernilai {b}")
print(f"Variabel c bernilai {c}")

print(f"Apakah a sama dengan b? Jawabannya adalah {sama_dengan_ab}")
print(f"Apakah b lebih besar dari c? Jawabannya adalah {lebih_besar_bc}")
print(f"Apakah a tidak sama dengan c? Jawabannya adalah {tidak_sama_ac}")
